def add(a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b


a = int(input("Please enter one number - >"))
b = int(input("Please enter another number - >")) 

print("Addition of both the number is - >",add(a,b))
print("Subtraction of both the number is - >",sub(a,b))
print("Multiplication of both the number is - >",mul(a,b))